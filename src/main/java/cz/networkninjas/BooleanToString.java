package cz.networkninjas;

/*
 * Complete the method that takes a boolean value and return a "true" or "false" string equivalent.
 * Vytvoř metodu, která přijme boolean hodnotu a vrátí odpovídající řetězec "true" nebo "false".
 *
 * Examples:
 * true  --> "true"
 * false --> "false"
 */
public class BooleanToString {
    public static String convert(boolean b) {
        return String.valueOf(b);
    }
}
